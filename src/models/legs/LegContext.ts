import type { Leg } from './Leg.js'

export interface LegContext {
	leg: Leg

	index: number

	previous?: Leg
	next?: Leg

	isLast: boolean

	/**
	 * Объемлющие непробитые ноги, упорядоченные от ближней [0] к самой
	 * внешней [last].
	 *
	 * Нога `j < i` считается объемлющей для `i`, если её защищаемый
	 * экстремум (`start.price`) не пробит ни одной ногой в окне `(j, i]`:
	 *   - bearish `j` (start = HH): пробой = любая точка строго выше start.price
	 *   - bullish `j` (start = LL): пробой = любая точка строго ниже start.price
	 *
	 * Пробой проверяется по `start`/`end` структурных ног (они берутся из
	 * Pivot-точек, сравнение по `high`/`low`) — отдельная свечная проверка
	 * не нужна.
	 *
	 * Сама нога `i` включается в окно проверки: если её `end` пробивает
	 * protected `j` — `j` вылетает из enclosingLegs для `i` (уровень потерял
	 * значимость к моменту формирования `i`).
	 *
	 * Движок НЕ решает, какую именно ногу считать «якорной» — это выбор
	 * потребителя (FibEngine / debug). Ближняя [0] и внешняя [last] обе
	 * доступны; выбор зависит от контекста.
	 */
	enclosingLegs: Leg[]

	/**
	 * Обратная карта: ноги, для которых текущая является объемлющей
	 * (т.е. те `i`, у которых эта нога есть в `enclosingLegs`).
	 */
	insideLegs: Leg[]
}
