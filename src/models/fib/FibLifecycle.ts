// FibLifecycle.ts
//
// Модель жизненного цикла Fib-сетапа: симуляция плейбука по каждой сетке
// (вход в зоне ретрейса, стоп за 0%, цели на расширениях) и запись исхода.
// Это исследовательская статистика, не торговый движок: никакого управления
// позицией, только фиксация того, что сделала цена.

import type { FibAnchorMode, FibDirection } from './FibGrid.js'
import type { StructureEventType } from '@/models/events/StructureEvent.js'

/**
 * Сценарии входа из плейбука:
 * - 'ote'     — ретрейс в зону 61.8–78.6, вход по первому касанию (78.6);
 * - 'deep'    — глубокий откат в зону 23.6–38.2, вход по касанию 38.2;
 * - 'breaker' — цена после события дошла до 141 БЕЗ касания OTE-зоны,
 *               вход на ретесте 100% (точки слома). Запись создаётся только
 *               если предусловие (141 раньше OTE) выполнилось.
 * - 'fade141' — контртренд: вход ПРОТИВ сетки по первому касанию 141
 *               (ближняя граница зоны расширения 1.41–1.61), цели вниз по
 *               сетке: TP1 = 100%, TP2 = 78.6. Направление сделки в outcome
 *               ИНВЕРТИРОВАНО относительно направления сетки.
 * - 'fade241' — то же от зоны 2.41–2.61: вход по касанию 241, стоп за 261.
 *
 * Волна 1 доработок fade (гипотезы пользователя после первых прогонов —
 * короткий стоп у fade съедается издержками, вход по первому касанию 141
 * ловит нож):
 * - 'fade241n' — вход по касанию 241 как fade241, но БЛИЖНИЕ цели:
 *                TP1 = 141, TP2 = 100 (EV_full = «фулл на 141»);
 * - 'fade200'  — вход по касанию 200 (фильтр «прошили 141 — не ловим»),
 *                стоп за 241 (+ запас), цели TP1 = 100%, TP2 = 78.6.
 *
 * Волна 2 — механизация «глаза» (данные: sweep-разрез стабильно лучше,
 * вход по первому касанию ловит нож): подтверждение закрытием свечи.
 * После касания уровня ждём первую свечу, ЗАКРЫВШУЮСЯ обратно на «нашей»
 * стороне уровня, вход по цене её закрытия (не по уровню — честная цена).
 * Если цена прошила стоп до подтверждения — invalidated (фильтр сработал).
 * - 'fade141c'  — как fade141 far (стоп за 200 + 0.5 ATR), но с подтверждением;
 * - 'fade241nc' — как fade241n (цели 141 → 100, стоп за 261 + 0.5 ATR),
 *                 но с подтверждением.
 *
 * Волна 3 — вариации breaker (гипотезы пользователя):
 * - 'breaker161' — как breaker, но если цена до входа (ретеста 100%) уходит
 *                  за 161, сетап ОТМЕНЯЕТСЯ (state 'expired'): фильтр
 *                  «слишком сильный импульс — ретест уже не тот».
 *                  Конфликт в одном баре (касание 100 и 161) — консервативно
 *                  отмена.
 * - 'breaker78'  — то же предусловие (141 раньше OTE-зоны), но вход глубже:
 *                  на 78.6 вместо 100. Реже входит, зато шире запас до стопа
 *                  и лучше цена. Симулируется со стопом 'zero'.
 *
 * Волна 4 — добор (scale-in): позиция делится 50/50, первая половина на
 * обычном входе сценария, вторая — на уровне медианной просадки победителей
 * (данные MAE волны 3). 1R = плановый риск ПОЛНОЙ позиции (обе половины);
 * если добор не сработал, лосс МЕНЬШЕ −1R (задействована половина риска),
 * поле rStop несёт фактический лосс, exposure — фактическую долю позиции.
 * Добор действует только ДО TP1; конфликт добора и TP1 в одном баре —
 * консервативно TP первым (без добора), добора и стопа — добор первым
 * (полный лосс).
 * - 'oteScale'     — вход 78.6, добор 50, стоп за 0;
 * - 'deepScale'    — вход 38.2, добор 23.6, стоп за 0;
 * - 'breakerScale' — вход 100 (ретест), добор 78.6, стоп за 0.
 */
export type FibScenario =
	| 'ote' | 'deep' | 'breaker' | 'breaker161' | 'breaker78'
	| 'oteScale' | 'deepScale' | 'breakerScale'
	| 'fade141' | 'fade241' | 'fade241n' | 'fade200'
	| 'fade141c' | 'fade241nc'

/**
 * Режим стопа:
 * - 'zero'    — стоп за 0% сетки (базовый плейбук);
 * - 'tight'   — стоп за 23.6 (только для OTE-входа от 78.6 — гипотеза
 *   пользователя, что укороченный стоп даёт лучшее матожидание);
 * - 'wide05'  — стоп за 0% с буфером 0.5 × ATR14 на момент пробоя
 *   (ote/deep; ATR восстанавливается как legSize / legAtrRatio);
 * - 'wide10'  — то же с буфером 1.0 × ATR14;
 * - 'zone'    — только fade: стоп за дальней границей зоны (fade141: 161,
 *   fade241/fade241n: 261, fade200: 241);
 * - 'zoneAtr' — только fade: стоп за той же границей + 0.5 × ATR14;
 * - 'far'     — только fade141: стоп за 200 + 0.5 × ATR14 — широкий стоп,
 *   лечащий проблему издержек на коротком зонном стопе;
 * - 'zero200' — тренд-сценарии (ote/deep): стоп за 0% как 'zero', но
 *   TP2 = 200 вместо 241 (гипотеза по данным ext200Pct > ext241Pct).
 * Breaker: 'zero' (базовый) + волна 3 — 'tight' (стоп за 23.6 при входе от
 * 100: тест сокращения стопа по данным MAE победителей). breaker161 и
 * breaker78 — только 'zero'. ATR-зависимые режимы (wide05/wide10/zoneAtr/far)
 * не эмитятся, если legAtrRatio недоступен.
 */
export type FibStopMode = 'zero' | 'tight' | 'wide05' | 'wide10' | 'zone' | 'zoneAtr' | 'far' | 'zero200'

/**
 * Финальное состояние сетапа:
 * - 'no-entry'    — вход не случился до конца данных;
 * - 'expired'     — до входа подтвердилось событие противоположного
 *                   направления (структура развернулась, сетап отменён);
 * - 'invalidated' — цена ушла за 0% до входа (для breaker: до ретеста 100%);
 * - 'open'        — вход есть, но ни стоп, ни TP2 не сработали до конца данных;
 * - 'stopped'     — после входа коснулись стопа (за 0%) раньше TP2;
 * - 'tp2'         — после входа дошли до TP2 (241) раньше стопа.
 *
 * TP1 (141) записывается отдельным флагом: состояние 'stopped' с tp1Hit=true —
 * это «дошли до первой цели, потом вернулись в стоп».
 */
export type FibSetupState = 'no-entry' | 'expired' | 'invalidated' | 'open' | 'stopped' | 'tp2'

/** Исход одного сетапа (кандидат × вариант якоря × сценарий × режим стопа). */
export interface FibSetupOutcome {
	candidateId: string
	variantMode: FibAnchorMode
	scenario: FibScenario
	stopMode: FibStopMode
	trigger: Exclude<StructureEventType, 'unlabeled'>
	direction: FibDirection
	/** Нога 0%→100% в ATR на момент пробоя — для фильтра в UI. */
	legAtrRatio: number | null
	/** Свип противоположного экстремума перед сломом (стоп-хант) — A/B-разрез. */
	oppositeSweptBefore: boolean
	createdAtIndex: number

	entered: boolean
	entryIndex: number | null
	entryPrice: number | null
	stopPrice: number
	/** |entry − stop| — размер риска (1R) в цене. */
	riskSize: number | null

	state: FibSetupState
	tp1Hit: boolean
	tp1Index: number | null
	tp2Hit: boolean
	tp2Index: number | null
	stopIndex: number | null

	/**
	 * Расстояние до TP1/TP2 от входа в единицах риска (R-мультипликаторы целей).
	 * Заполняются при входе — нужны для расчёта EV в UI.
	 * Для scale-сценариев «вход» = средневзвешенная цена фактически набранной
	 * позиции, R — в единицах ПЛАНОВОГО риска, значения масштабированы на
	 * exposure.
	 */
	rTp1: number | null
	rTp2: number | null
	/**
	 * Волна 4: фактический результат стопа в плановых R. Для обычных сценариев
	 * и полностью набранных scale-позиций = −1; если добор не сработал —
	 * меньше по модулю (задействована часть риска). null — стопа не было.
	 * Агрегация должна использовать (rStop ?? −1) вместо константы −1.
	 */
	rStop: number | null
	/**
	 * Волна 4: фактически набранная доля позиции (0.5 — только первая
	 * половина, 1 — полная). Для обычных сценариев всегда 1. Масштабирует
	 * издержки (объём филлов) в fibCosts.
	 */
	exposure: number | null
	/**
	 * Поведение после TP1 при менеджменте «безубыток»: вернулась ли цена
	 * к цене входа раньше, чем дошла до TP2. true — раннер закрыт в 0,
	 * false — дошли до TP2, null — TP1 не был достигнут или данные кончились.
	 * Бар касания TP1 проверяется консервативно: если его противоположная
	 * тень задела вход, считаем возврат (внутрибарная последовательность неизвестна).
	 */
	beAfterTp1: boolean | null

	/** Максимальный ход в плюс/минус после входа, в R. */
	mfeR: number | null
	maeR: number | null
	/**
	 * Максимальная достигнутая цена после входа как ratio сетки:
	 * (price − p0) / legSize × 100 (для short-сетки зеркально). Окно: от входа
	 * до первого касания ИСХОДНОГО стопа сценария либо конца данных — TP2 скан
	 * не обрывает. Диагностическое поле для анализа целей 141/200/241 и
	 * трейлинга, в EV не участвует. Только для ote/deep/breaker; fade — null.
	 */
	maxExtensionRatio: number | null
	/**
	 * «Стоп выбит, затем TP1 всё же достигнут»: для state === 'stopped'
	 * сканируем после стоп-бара, дошла ли цена до TP1 раньше подтверждения
	 * следующего события против направления СДЕЛКИ либо конца данных.
	 * Для остальных состояний — null. Диагностика качества стопа.
	 */
	tpAfterStop: boolean | null
	barsToEntry: number | null
	/** Баров от входа до финала (stop/tp2) либо до конца данных. */
	barsToResolve: number | null
}

/**
 * Гистограмма досягаемости: куда цена реально ходит после подтверждения
 * BOS/CHoCH — независимо от сценариев входа. Одна запись на кандидат × якорь.
 * Окно замера: от createdAtIndex + 1 до подтверждения первого противоположного
 * события либо конца данных. Всё в ratio сетки (проценты, для short зеркально):
 * 100 = точка слома, 0 = основание ноги, 141/200/241 = расширения.
 * Назначение: увидеть распределение глубины ретрейса и потолка расширения,
 * чтобы ставить входы и цели по данным, а не перебором уровней.
 */
export interface FibReachRecord {
	candidateId: string
	variantMode: FibAnchorMode
	trigger: Exclude<StructureEventType, 'unlabeled'>
	direction: FibDirection
	legAtrRatio: number | null
	oppositeSweptBefore: boolean
	/** Минимальный достигнутый ratio (глубина ретрейса к 0%). */
	minRetraceRatio: number
	/** Максимальный достигнутый ratio (потолок расширения). */
	maxExtensionRatio: number
	/** Баров в окне замера (для оценки длины жизни сетки). */
	windowBars: number
	/**
	 * Fade-reach: куда цена идёт ПОСЛЕ первого касания 141 (в том же окне).
	 * pullback — минимальный ratio после касания (глубина отката против
	 * сетки — потенциал fade-сделки), extension — максимальный ratio после
	 * касания (продолжение — риск fade-сделки). null = 141 не был достигнут.
	 */
	after141: { pullbackRatio: number; extensionRatio: number } | null
	/** То же после первого касания 241. null = 241 не был достигнут. */
	after241: { pullbackRatio: number; extensionRatio: number } | null
}

export interface FibLifecycleResult {
	outcomes: FibSetupOutcome[]
	/** Досягаемость по каждому кандидату — независимая диагностика уровней. */
	reach: FibReachRecord[]
}
